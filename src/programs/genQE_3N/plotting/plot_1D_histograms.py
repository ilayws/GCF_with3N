def plot_multiple_ratios(ratio_pairs, weight_filter_percentile=98.0, title=None):
    """Plot multiple ratio graphs together on the same figure
    
    Args:
        ratio_pairs: List of tuples [(var1, var2), ...] where ratio = var1 / var2
        weight_filter_percentile: Filter out weights above this percentile
        title: Optional custom title for the plot
        
    Example usage:
        plot_multiple_ratios([("xB star", "xB"), ("xB rocket", "xB")])
        plot_multiple_ratios([("Q2 star", "Q2"), ("Q2 rocket", "Q2")], title="Q2 Comparisons")
    """
    if not ratio_pairs:
        print("No ratio pairs provided")
        return
    
    # Use a better gradient for multiple ratios
    import matplotlib.cm as cm
    import matplotlib.colors as mcolors
    n_curves = len(ratio_pairs)
    cmap = cm.get_cmap('plasma')  # plasma, inferno, turbo, or rainbow are good alternatives to viridis
    colors = [cmap(i / max(n_curves - 1, 1)) for i in range(n_curves)]

    plt.figure(figsize=(10, 6))

    # Store x-axis limits for consistent plotting
    all_x_mins, all_x_maxs = [], []

    for i, (var1_name, var2_name) in enumerate(ratio_pairs):
        # Load both histograms (always use total weight for ratios)
        x1, y1 = load_histogram(var1_name, use_average_weight=False, weight_filter_percentile=weight_filter_percentile)
        x2, y2 = load_histogram(var2_name, use_average_weight=False, weight_filter_percentile=weight_filter_percentile)

        if x1 is None or x2 is None:
            print(f"Error: Could not load histograms for {var1_name} and/or {var2_name}")
            continue

        # Check if the histograms have the same binning
        if len(x1) != len(x2) or not np.allclose(x1, x2, rtol=1e-10):
            print(f"Warning: {var1_name} and {var2_name} have different binning.")
            try:
                from scipy.interpolate import interp1d
                print("Attempting interpolation using scipy...")
                # Interpolate y2 to match x1 binning
                f = interp1d(x2, y2, kind='linear', bounds_error=False, fill_value=0)
                y2_interp = f(x1)
                x_common = x1
                y1_common = y1
                y2_common = y2_interp
            except ImportError:
                print("Scipy not available. Using simple resampling (may be less accurate).")
                # Simple fallback: use the variable with fewer bins as the common binning
                if len(x1) <= len(x2):
                    x_common = x1
                    y1_common = y1
                    y2_common = np.interp(x1, x2, y2)
                else:
                    x_common = x2
                    y2_common = y2
                    y1_common = np.interp(x2, x1, y1)
        else:
            x_common = x1
            y1_common = y1
            y2_common = y2

        # Calculate ratio, avoiding division by zero
        ratio = np.divide(y1_common, y2_common, out=np.zeros_like(y1_common), where=y2_common!=0)

        # Plot the ratio with gradient color
        color = colors[i]
        label = f'{var1_name} / {var2_name}'
        plt.plot(x_common, ratio, '-', color=color, linewidth=2, label=label)

        # Collect x-axis limits for consistent plotting
        max_combined = np.max([np.max(y1_common), np.max(y2_common)])
        threshold = 0.01 * max_combined
        significant_mask = (y1_common > threshold) | (y2_common > threshold)
        if np.any(significant_mask):
            x_min, x_max = x_common[significant_mask].min(), x_common[significant_mask].max()
            all_x_mins.append(x_min)
            all_x_maxs.append(x_max)
    
    # Set consistent x-axis limits across all ratios
    if all_x_mins and all_x_maxs:
        x_min_global = min(all_x_mins)
        x_max_global = max(all_x_maxs)
        x_range = x_max_global - x_min_global
        padding = 0.05 * x_range if x_range > 0 else 0.1 * abs(x_max_global)
        plt.xlim(x_min_global - padding, x_max_global + padding)
    
    # Get variable info for x-axis label (use first ratio pair)
    if ratio_pairs:
        info1 = get_variable_info(ratio_pairs[0][0])
        plt.xlabel(info1['label'], fontsize=14)
    
    plt.ylabel('Ratio', fontsize=14)
    
    # Set title
    if title:
        plot_title = title
    else:
        if len(ratio_pairs) == 1:
            plot_title = f'Ratio: {ratio_pairs[0][0]} / {ratio_pairs[0][1]}'
        else:
            plot_title = 'Multiple Ratio Comparison'
    
    filter_note = " (filtered)" if weight_filter_percentile < 100 else ""
    plt.title(f'{plot_title}{filter_note}', fontsize=16)
    plt.grid(True, alpha=0.3)
    plt.legend()
    plt.tight_layout()
    
    # Save the plot
    filter_suffix = f'_f{weight_filter_percentile}' if weight_filter_percentile < 100 else ''
    # Create filename from ratio pairs with safe characters
    ratio_names = '_'.join([f"{r[0].replace(' ', '_').replace('.', '')}_over_{r[1].replace(' ', '_').replace('.', '')}" for r in ratio_pairs])
    output_dir = "analysis_output/png_files/ratio_plots"
    os.makedirs(output_dir, exist_ok=True)
    plt.savefig(f'{output_dir}/plot_multiple_ratios_{ratio_names}{filter_suffix}.png', dpi=300, bbox_inches='tight')
    plt.close()
    
    # Print statistics for each ratio
    print(f"\nStatistics for multiple ratios:")
    for var1_name, var2_name in ratio_pairs:
        x1, y1 = load_histogram(var1_name, use_average_weight=False, weight_filter_percentile=weight_filter_percentile)
        x2, y2 = load_histogram(var2_name, use_average_weight=False, weight_filter_percentile=weight_filter_percentile)
        
        if x1 is not None and x2 is not None:
            # Recalculate ratio for statistics (same logic as above)
            if len(x1) != len(x2) or not np.allclose(x1, x2, rtol=1e-10):
                if len(x1) <= len(x2):
                    y2_common = np.interp(x1, x2, y2)
                    y1_common = y1
                else:
                    y1_common = np.interp(x2, x1, y1)
                    y2_common = y2
            else:
                y1_common = y1
                y2_common = y2
            
            ratio = np.divide(y1_common, y2_common, out=np.zeros_like(y1_common), where=y2_common!=0)
            valid_ratio_mask = (y2_common != 0) & np.isfinite(ratio)
            
            if np.any(valid_ratio_mask):
                valid_ratios = ratio[valid_ratio_mask]
                print(f"  {var1_name} / {var2_name}:")
                print(f"    Mean: {np.mean(valid_ratios):.4f}, Median: {np.median(valid_ratios):.4f}")
                print(f"    Min: {np.min(valid_ratios):.4f}, Max: {np.max(valid_ratios):.4f}")

