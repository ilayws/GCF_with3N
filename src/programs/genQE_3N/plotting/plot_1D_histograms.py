def discover_histogram_files():
    """Discover all available 1D histogram files and extract variable names"""
    # Look in the analysis_output/txt_files directory
    pattern = "analysis_output/txt_files/hist_*_1D.txt"
    files = glob.glob(pattern)
    
    # Extract variable names from filenames
    variables = []
    for filepath in files:
        # Get just the filename without the directory path
        filename = os.path.basename(filepath)
        # Remove 'hist_' prefix and '_1D.txt' suffix
        var_name = filename.replace('hist_', '').replace('_1D.txt', '')
        variables.append(var_name)
    
    return sorted(variables)  # Sort for consistent ordering

def load_histogram(var_name, use_average_weight=False, weight_filter_percentile=98.0):
    """Load histogram data for a given variable name
    
    Args:
        var_name: Variable name (e.g., 'xB', 'Q2')
        use_average_weight: If True, return weight/count, if False return total weight
        weight_filter_percentile: Filter out weights above this percentile (default 99.5%)
    """
    filename = f"analysis_output/txt_files/hist_{var_name}_1D.txt"
    if not os.path.exists(filename):
        print(f"Warning: File {filename} not found")
        return None, None
    
    data = np.loadtxt(filename, comments='#')
    if data.size == 0:
        print(f"Warning: No data in {filename}")
        return None, None
    
    x = data[:, 0]  # bin centers
    if data.shape[1] >= 3:
        # File has format: bin_center, total_weight, count
        total_weight = data[:, 1]
        count = data[:, 2]
        
        if use_average_weight:
            # Calculate average weight per event, avoid division by zero
            y = np.divide(total_weight, count, out=np.zeros_like(total_weight), where=count!=0)
        else:
            y = total_weight
    else:
        # File has format: bin_center, weight (assume count=1)
        y = data[:, 1]
        if use_average_weight:
            print(f"Warning: {filename} doesn't have count data, showing total weight")
    
    # Filter out weights that are too big
    if weight_filter_percentile < 100:
        # Only filter if there are non-zero weights
        non_zero_weights = y[y > 0]
        if len(non_zero_weights) > 0:
            weight_threshold = np.percentile(non_zero_weights, weight_filter_percentile)
            original_max = np.max(y)
            
            # Remove bins with weights above the threshold (don't just clip)
            mask = y <= weight_threshold
            n_filtered = int(np.sum(~mask))
            if n_filtered > 0:
                print(f"  {var_name}: Removed {n_filtered} bins with weights > {weight_threshold:.3e} (was {original_max:.3e})")

            # Apply mask to both x and y
            x = x[mask]
            y = y[mask]

            # If all bins were removed, return None so caller can skip plotting
            if y.size == 0:
                print(f"  {var_name}: All bins removed by filtering; no data to plot.")
                return None, None
    
    return x, y

