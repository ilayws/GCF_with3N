def discover_histogram_files():
    """Discover all available 1D histogram files and extract variable names"""
    # Look in the analysis_output_2N/txt_files directory
    pattern = "analysis_output_2N/txt_files/hist_*_1D.txt"
    files = glob.glob(pattern)
    
    # Extract variable names from filenames
    variables = []
    for filepath in files:
        # Get just the filename without the directory path
        filename = os.path.basename(filepath)
        # Remove 'hist_' prefix and '_1D.txt' suffix
        var_name = filename.replace('hist_', '').replace('_1D.txt', '')
        variables.append(var_name)
    
    return sorted(variables)  # Sort for consistent ordering

def load_histogram(var_name, use_average_weight=False, weight_filter_percentile=98.0):
    """Load histogram data for a given variable name
    
    Args:
        var_name: Variable name (e.g., 'xB', 'Q2')
        use_average_weight: If True, return weight/count, if False return total weight
        weight_filter_percentile: Filter out weights above this percentile (default 98%)
    """
    filename = f"analysis_output_2N/txt_files/hist_{var_name}_1D.txt"
    if not os.path.exists(filename):
        print(f"Warning: File {filename} not found")
        return None, None
    
    data = np.loadtxt(filename, comments='#')
    if data.size == 0:
        print(f"Warning: No data in {filename}")
        return None, None
    
    x = data[:, 0]  # bin centers
    if data.shape[1] >= 3:
        # File has format: bin_center, total_weight, count
        total_weight = data[:, 1]
        count = data[:, 2]
        
        if use_average_weight:
            # Calculate average weight per event, avoid division by zero
            y = np.divide(total_weight, count, out=np.zeros_like(total_weight), where=count!=0)
        else:
            y = total_weight
    else:
        # File has format: bin_center, weight (assume count=1)
        y = data[:, 1]
        if use_average_weight:
            print(f"Warning: {filename} doesn't have count data, showing total weight")
    
    # Filter out weights that are too big
    if weight_filter_percentile < 100:
        # Only filter if there are non-zero weights
        non_zero_weights = y[y > 0]
        if len(non_zero_weights) > 0:
            weight_threshold = np.percentile(non_zero_weights, weight_filter_percentile)
            original_max = np.max(y)
            
            # Remove bins with weights above the threshold
            mask = y <= weight_threshold
            n_filtered = int(np.sum(~mask))
            if n_filtered > 0:
                print(f"  {var_name}: Removed {n_filtered} bins with weights > {weight_threshold:.3e} (was {original_max:.3e})")

            # Apply mask to both x and y
            x = x[mask]
            y = y[mask]

            # If all bins were removed, return None so caller can skip plotting
            if y.size == 0:
                print(f"  {var_name}: All bins removed by filtering; no data to plot.")
                return None, None
    
    return x, y

def get_variable_info(var_name):
    """Get display information for variables - provides sensible defaults for unknown variables"""
    # Common color palette for consistent plotting
    colors = ['blue', 'red', 'green', 'orange', 'purple', 'brown', 'pink', 'gray', 
              'olive', 'cyan', 'magenta', 'lime', 'navy', 'maroon', 'teal', 'gold']
    
    # Predefined info for known variables
    known_vars = {
        'xB': {'label': r'$x_B$', 'color': 'blue'},
        'Q2': {'label': r'$Q^2$ (GeV$^2$)', 'color': 'green'},
        'theta12': {'label': r'$\theta_{12}$ (degrees)', 'color': 'purple'},
        'Lead Nucleon Momentum': {'label': r'Lead Nucleon Momentum (GeV/c)', 'color': 'red'},
        'Recoil Nucleon Momentum': {'label': r'Recoil Nucleon Momentum (GeV/c)', 'color': 'orange'},
        'pmiss': {'label': r'$p_{miss}$ (GeV/c)', 'color': 'brown'},
    }
    
    if var_name in known_vars:
        return known_vars[var_name]
    else:
        # Generate a color based on variable name hash for consistency
        color_idx = hash(var_name) % len(colors)
        # Create a sensible label by replacing underscores and adding units if detected
        label = var_name.replace('_', ' ')
        if 'theta' in var_name.lower() or 'phi' in var_name.lower() or 'angle' in var_name.lower():
            label += ' (degrees)'
        elif 'mom' in var_name.lower() or var_name.startswith('p') and 'after' in var_name:
            label += ' (GeV/c)'
        elif var_name == 'Q2':
            label = r'$Q^2$ (GeV$^2$)'
        return {'label': label, 'color': colors[color_idx]}

def plot_single_histogram(var_name, use_average_weight=False, weight_filter_percentile=98.0, xlim=None):
    """Plot a single histogram with optional x-axis limits"""
    x, y = load_histogram(var_name, use_average_weight, weight_filter_percentile)
    if x is None:
        return
    info = get_variable_info(var_name)
    plt.figure(figsize=(8, 6))
    plt.plot(x, y, '-', color=info['color'], linewidth=2)
    plt.xlabel(info['label'], fontsize=14)
    # Set appropriate y-label based on mode
    if use_average_weight:
        plt.ylabel('Average Weight per Event', fontsize=14)
        plot_type = 'Average Weight'
    else:
        plt.ylabel('Total Weight', fontsize=14)
        plot_type = 'Total Weight'
    # Set x-axis limits to only show significant data with small padding
    if xlim is not None:
        max_y = np.max(y)
        threshold = 0.01 * max_y
        significant_mask = y > threshold
        if np.any(significant_mask):
            x_min, x_max = x[significant_mask].min(), x[significant_mask].max()
            x_range = x_max - x_min
            padding = 0.05 * x_range if x_range > 0 else 0.1 * abs(x_max)
            plt.xlim(x_min - padding, x_max + padding)
        else:
            plt.xlim(xlim)
    else:
        max_y = np.max(y)
        threshold = 0.01 * max_y
        significant_mask = y > threshold
        if np.any(significant_mask):
            x_min, x_max = x[significant_mask].min(), x[significant_mask].max()
            x_range = x_max - x_min
            padding = 0.05 * x_range if x_range > 0 else 0.1 * abs(x_max)
            plt.xlim(x_min - padding, x_max + padding)
    filter_note = " (filtered)" if weight_filter_percentile < 100 else ""
    plt.title(f'1D Histogram: {info["label"]} ({plot_type}){filter_note}', fontsize=16)
    plt.grid(True, alpha=0.3)
    plt.tight_layout()
    # Save the plot with appropriate filename
    suffix = '_avg' if use_average_weight else '_total'
    filter_suffix = f'_f{weight_filter_percentile}' if weight_filter_percentile < 100 else ''
    # Replace spaces and special characters for safe filename
    safe_var_name = var_name.replace(' ', '_').replace('.', '')
    output_dir = "analysis_output_2N/png_files/individual_plots"
    os.makedirs(output_dir, exist_ok=True)
    plt.savefig(f'{output_dir}/plot_{safe_var_name}_1D{suffix}{filter_suffix}.png', dpi=300, bbox_inches='tight')
    plt.close()

