def load_fsi_stats(filepath="analysis_output_2N/txt_files/fsi_event_stats.txt"):
    """Load key-value FSI stats written by SRC_analysis_2N.cpp."""
    if not os.path.exists(filepath):
        return None

    stats = {}
    with open(filepath, 'r') as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith('#'):
                continue
            parts = line.split()
            if len(parts) < 2:
                continue
            key, val = parts[0], parts[1]
            try:
                if any(ch in val for ch in ['.', 'e', 'E']):
                    stats[key] = float(val)
                else:
                    stats[key] = int(val)
            except ValueError:
                continue
    return stats

def print_fsi_stats_summary(stats):
    """Print a compact FSI summary including CX and pion production stats."""
    if not stats:
        print("\nFSI stats file not found (analysis_output_2N/txt_files/fsi_event_stats.txt).")
        print("Run SRC_analysis_2N first to generate CX/pion statistics.")
        return

    accepted = int(stats.get('accepted_events', 0))
    cx_any = int(stats.get('events_with_any_cx', 0))
    cx_lead = int(stats.get('events_with_lead_cx', 0))
    cx_recoil = int(stats.get('events_with_recoil_cx', 0))
    cx_total = int(stats.get('total_cx_transitions', 0))
    pion_any = int(stats.get('events_with_any_pion', 0))
    pi_total = int(stats.get('total_pions', 0))
    pi_plus = int(stats.get('total_pi_plus', 0))
    pi_minus = int(stats.get('total_pi_minus', 0))
    pi_zero = int(stats.get('total_pi_zero', 0))
    secondaries = int(stats.get('total_secondaries', 0))

    frac_cx = stats.get('frac_events_with_any_cx', (cx_any / max(accepted, 1)))
    frac_pion = stats.get('frac_events_with_any_pion', (pion_any / max(accepted, 1)))
    avg_pions = stats.get('avg_pions_per_event', (pi_total / max(accepted, 1)))
    avg_secondaries = stats.get('avg_secondaries_per_event', (secondaries / max(accepted, 1)))

    print("\nFSI channel statistics (from fsi_event_stats.txt)")
    print("------------------------------------------------")
    print(f"Accepted events:         {accepted}")
    print(f"Events with any CX:      {cx_any} ({100.0 * frac_cx:.2f}%)")
    print(f"  Lead CX events:        {cx_lead}")
    print(f"  Recoil CX events:      {cx_recoil}")
    print(f"Total CX transitions:    {cx_total}")
    print(f"Events with any pion:    {pion_any} ({100.0 * frac_pion:.2f}%)")
    print(f"Total pions produced:    {pi_total} [pi+={pi_plus}, pi-={pi_minus}, pi0={pi_zero}]")
    print(f"Total secondaries:       {secondaries}")
    print(f"Avg secondaries/event:   {avg_secondaries:.4f}")
    print(f"Avg pions/event:         {avg_pions:.4f}")

