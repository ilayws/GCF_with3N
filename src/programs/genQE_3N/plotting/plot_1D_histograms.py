def gaussian(x, amplitude, mean, sigma, offset=0):
    """Gaussian function for curve fitting"""
    return amplitude * np.exp(-0.5 * ((x - mean) / sigma) ** 2) + offset

def fit_gaussian_to_peak(x, y, peak_fraction=0.1, min_r_squared=0.3):
    """Fit a Gaussian to the peak region of histogram data
    
    Args:
        x: x values (bin centers)
        y: y values (weights)
        peak_fraction: fraction of peak height to use for fitting region (0.1 = 10% of peak height)
        min_r_squared: minimum R-squared value to consider fit "good"
        
    Returns:
        dict with 'success', 'peak_center', 'r_squared', 'fit_params' or None if fit failed
    """
    if not SCIPY_AVAILABLE:
        print("    Scipy not available")
        return {
            'success': False,
            'peak_center': None,
            'r_squared': 0.0,
            'fit_params': None,
            'error': 'scipy not available'
        }
        
    if len(x) < 3 or len(y) < 3: 
        print(f"    Not enough points: x={len(x)}, y={len(y)}")
        return {
            'success': False,
            'peak_center': None,
            'r_squared': 0.0,
            'fit_params': None,
            'error': 'not enough points'
        }
    
    # Find peak
    peak_idx = np.argmax(y)
    peak_x = x[peak_idx]
    peak_y = y[peak_idx]
    
    if peak_y <= 0:
        print(f"    Peak value is non-positive: {peak_y}")
        return {
            'success': False,
            'peak_center': None,
            'r_squared': 0.0,
            'fit_params': None,
            'error': 'non-positive peak'
        }
    
    # Use a much broader fitting region - include points above peak_fraction of peak height
    # This will include many more points around the peak
    threshold = peak_fraction * peak_y
    fit_mask = y >= threshold
    
    # If too few points above threshold, use a fixed number of points around peak
    if np.sum(fit_mask) < 5:
        # Use points around the peak index - take up to 10 points on each side
        half_window = min(10, len(x) // 4)  # Use up to 1/4 of all points, max 10 on each side
        start_idx = max(0, peak_idx - half_window)
        end_idx = min(len(x), peak_idx + half_window + 1)
        fit_mask = np.zeros_like(y, dtype=bool)
        fit_mask[start_idx:end_idx] = True
    
    # Ensure we have at least 3 points for fitting
    if np.sum(fit_mask) < 3:
        print(f"    Not enough points for fitting: {np.sum(fit_mask)}")
        return {
            'success': False,
            'peak_center': None,
            'r_squared': 0.0,
            'fit_params': None,
            'error': 'insufficient fitting points'
        }
    
    x_fit = x[fit_mask]
    y_fit = y[fit_mask]
    
    print(f"    Fitting with {len(x_fit)} points, peak_y={peak_y:.3e}, x_range={x_fit.max() - x_fit.min():.3f}")
    
    try:
        # Initial guess: amplitude=peak_y, mean=peak_x, sigma=rough estimate, offset=min(y_fit)
        x_range = x_fit.max() - x_fit.min()
        initial_sigma = max(x_range / 6, (x[1] - x[0]) if len(x) > 1 else 1.0)  # More reasonable sigma guess
        initial_offset = np.min(y_fit) * 0.5  # Small offset
        
        # More lenient bounds
        lower_bounds = [
            0, 
            x_fit.min() - x_range,  
            (x[1] - x[0]) / 10 if len(x) > 1 else 0.001,  
            -peak_y  
        ]
        upper_bounds = [
            peak_y * 20,  
            x_fit.max() + x_range, 
            x_range * 2, 
            peak_y 
        ]
        
        # Suppress scipy warnings for cleaner output
        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            popt, _ = curve_fit(
                gaussian, x_fit, y_fit,
                p0=[peak_y, peak_x, initial_sigma, initial_offset],
                bounds=(lower_bounds, upper_bounds),
                maxfev=2000  # More iterations
            )
        
        # Calculate R-squared
        y_pred = gaussian(x_fit, *popt)
        ss_res = np.sum((y_fit - y_pred) ** 2)
        ss_tot = np.sum((y_fit - np.mean(y_fit)) ** 2)
        r_squared = 1 - (ss_res / ss_tot) if ss_tot > 0 else 0
        
        # TEMPORARY: Accept all fits regardless of quality to see what we get
        return {
            'success': True,  # Always accept the fit
            'peak_center': popt[1],  # mean parameter
            'r_squared': r_squared,
            'fit_params': popt
        }
    
    except Exception as e:
        # For debugging - let's see what's going wrong
        print(f"    Fitting failed with error: {e}")
        return {
            'success': False,
            'peak_center': None,
            'r_squared': 0.0,
            'fit_params': None,
            'error': str(e)
        }

def plot_single_histogram(var_name, use_average_weight=False, weight_filter_percentile=98.0, xlim=None):
    """Plot a single histogram with optional x-axis limits"""
    x, y = load_histogram(var_name, use_average_weight, weight_filter_percentile)
    if x is None:
        return
    info = get_variable_info(var_name)
    plt.figure(figsize=(8, 6))
    plt.plot(x, y, '-', color=info['color'], linewidth=2)
    plt.xlabel(info['label'], fontsize=14)
    # Set appropriate y-label based on mode
    if use_average_weight:
        plt.ylabel('Average Weight per Event', fontsize=14)
        plot_type = 'Average Weight'
    else:
        plt.ylabel('Total Weight', fontsize=14)
        plot_type = 'Total Weight'
    # Set x-axis limits to only show significant data (above threshold) with small padding
    if xlim is not None:
        # Only use nonzero or significant bins for xlim
        max_y = np.max(y)
        threshold = 0.01 * max_y
        significant_mask = y > threshold
        if np.any(significant_mask):
            x_min, x_max = x[significant_mask].min(), x[significant_mask].max()
            x_range = x_max - x_min
            padding = 0.05 * x_range if x_range > 0 else 0.1 * abs(x_max)
            plt.xlim(x_min - padding, x_max + padding)
        else:
            plt.xlim(xlim)
    else:
        max_y = np.max(y)
        threshold = 0.01 * max_y  # Consider data below 1% of max as insignificant
        significant_mask = y > threshold
        if np.any(significant_mask):
            x_min, x_max = x[significant_mask].min(), x[significant_mask].max()
            x_range = x_max - x_min
            # Fixed alpha plot limits and ticks (use exact ranges requested)
            alpha_limits = {
                'alpha_q': (-1.6, -0.2),
                'alpha_deutron': (1.0, 2.0),
                'alpha_p_final': (0.0, 1.0),
                'alpha_p_initial': (1.0, 2.0)
            }
            if var_name in alpha_limits:
                left, right = alpha_limits[var_name]
                plt.xlim(left, right)
                try:
                    ticks = np.arange(left, right + 1e-9, 0.1)
                    plt.xticks(ticks)
                    ax = plt.gca()
                    try:
                        if ax.get_yscale() == 'linear':
                            ax.set_aspect('equal', adjustable='box')
                    except Exception:
                        pass
                except Exception:
                    pass
            else:
                padding = 0.05 * x_range if x_range > 0 else 0.1 * abs(x_max)
                plt.xlim(x_min - padding, x_max + padding)
    filter_note = " (filtered)" if weight_filter_percentile < 100 else ""
    plt.title(f'1D Histogram: {info["label"]} ({plot_type}){filter_note}', fontsize=16)
    plt.grid(True, alpha=0.3)
    plt.tight_layout()
    # Save the plot with appropriate filename
    suffix = '_avg' if use_average_weight else '_total'
    filter_suffix = f'_f{weight_filter_percentile}' if weight_filter_percentile < 100 else ''
    # Replace spaces and special characters for safe filename
    safe_var_name = var_name.replace(' ', '_').replace('.', '')
    output_dir = "analysis_output/png_files/individual_plots"
    os.makedirs(output_dir, exist_ok=True)
    plt.savefig(f'{output_dir}/plot_{safe_var_name}_1D{suffix}{filter_suffix}.png', dpi=300, bbox_inches='tight')
    # If this is an alpha plot, show it interactively before closing
    alpha_keys = {'alpha_q', 'alpha_deutron', 'alpha_p_final', 'alpha_p_initial', 'alpha_sum'}
    if var_name in alpha_keys:
        try:
            plt.show()
        except Exception:
            pass
    plt.close()

def plot_ratio(var1_name, var2_name, weight_filter_percentile=98.0):
    """Plot the ratio of total weights between two variables (var1 / var2)
    
    Args:
        var1_name: First variable name (numerator)
        var2_name: Second variable name (denominator)
        weight_filter_percentile: Filter out weights above this percentile
    """
    # Load both histograms (always use total weight for ratios)
    x1, y1 = load_histogram(var1_name, use_average_weight=False, weight_filter_percentile=weight_filter_percentile)
    x2, y2 = load_histogram(var2_name, use_average_weight=False, weight_filter_percentile=weight_filter_percentile)
    
    if x1 is None or x2 is None:
        print(f"Error: Could not load histograms for {var1_name} and/or {var2_name}")
        return
    
    # Check if the histograms have the same binning
    if len(x1) != len(x2) or not np.allclose(x1, x2, rtol=1e-10):
        print(f"Warning: {var1_name} and {var2_name} have different binning.")
        try:
            from scipy.interpolate import interp1d
            print("Attempting interpolation using scipy...")
            # Interpolate y2 to match x1 binning
            f = interp1d(x2, y2, kind='linear', bounds_error=False, fill_value=0)
            y2_interp = f(x1)
            x_common = x1
            y1_common = y1
            y2_common = y2_interp
        except ImportError:
            print("Scipy not available. Using simple resampling (may be less accurate).")
            # Simple fallback: use the variable with fewer bins as the common binning
            if len(x1) <= len(x2):
                x_common = x1
                y1_common = y1
                # Simple nearest-neighbor resampling for y2
                y2_common = np.interp(x1, x2, y2)
            else:
                x_common = x2
                y2_common = y2
                # Simple nearest-neighbor resampling for y1
                y1_common = np.interp(x2, x1, y1)
    else:
        x_common = x1
        y1_common = y1
        y2_common = y2
    
    # Calculate ratio, avoiding division by zero
    ratio = np.divide(y1_common, y2_common, out=np.zeros_like(y1_common), where=y2_common!=0)
    
    # Get variable info for labels
    info1 = get_variable_info(var1_name)
    info2 = get_variable_info(var2_name)
    
    plt.figure(figsize=(8, 6))
    plt.plot(x_common, ratio, '-', color='black', linewidth=2)
    plt.xlabel(f'{info1["label"]} (same binning as {info2["label"]})', fontsize=14)
    plt.ylabel(f'Ratio: {var1_name} / {var2_name}', fontsize=14)
    
    # Set x-axis limits to show significant data
    # Use the maximum of both distributions to define significance
    max_combined = np.max([np.max(y1_common), np.max(y2_common)])
    threshold = 0.01 * max_combined
    significant_mask = (y1_common > threshold) | (y2_common > threshold)
    if np.any(significant_mask):
        x_min, x_max = x_common[significant_mask].min(), x_common[significant_mask].max()
        x_range = x_max - x_min
        padding = 0.05 * x_range if x_range > 0 else 0.1 * abs(x_max)
        plt.xlim(x_min - padding, x_max + padding)
    
    filter_note = " (filtered)" if weight_filter_percentile < 100 else ""
    plt.title(f'Ratio: {var1_name} / {var2_name}{filter_note}', fontsize=16)
    plt.grid(True, alpha=0.3)
    plt.tight_layout()
    
    # Save the plot
    filter_suffix = f'_f{weight_filter_percentile}' if weight_filter_percentile < 100 else ''
    # Replace spaces and special characters for safe filename
    safe_var1 = var1_name.replace(' ', '_').replace('.', '')
    safe_var2 = var2_name.replace(' ', '_').replace('.', '')
    output_dir = "analysis_output/png_files/ratio_plots"
    os.makedirs(output_dir, exist_ok=True)
    plt.savefig(f'{output_dir}/plot_ratio_{safe_var1}_over_{safe_var2}{filter_suffix}.png', dpi=300, bbox_inches='tight')
    plt.close()
    
    # Print some statistics
    valid_ratio_mask = (y2_common != 0) & np.isfinite(ratio)
    if np.any(valid_ratio_mask):
        valid_ratios = ratio[valid_ratio_mask]
        print(f"\nRatio statistics for {var1_name} / {var2_name}:")
        print(f"  Mean ratio: {np.mean(valid_ratios):.4f}")
        print(f"  Median ratio: {np.median(valid_ratios):.4f}")
        print(f"  Min ratio: {np.min(valid_ratios):.4f}")
        print(f"  Max ratio: {np.max(valid_ratios):.4f}")
        print(f"  Std ratio: {np.std(valid_ratios):.4f}")

